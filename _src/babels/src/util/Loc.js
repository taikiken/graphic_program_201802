'use strict';

// window.location に関する Utility

let _symbol = Symbol();

export class Loc {
  /**
   * static class です、instance を作成できません
   * @param {Symbol} target Singleton を実現するための private symbol
   */
  constructor( target ) {

    if ( _symbol !== target ) {

      throw new Error( `Loc is singleton pattern. not use new Api().` );

    }

  }
}
