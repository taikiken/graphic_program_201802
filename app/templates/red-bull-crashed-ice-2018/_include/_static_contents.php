  <div class="about__text">
    <p>アイスクロス・ダウンヒル「Red Bull Crashed Ice」は2001年の初開催以来、世界トップレベルのスリルを誇るウィンタースポーツイベントへと成長を重ねてきた。最長600mのコースを4人のライダーがひしめき合いながら勝利を目指して滑り下りていくこのレースは、シケイン、ジャンプ、ローラーなどが配置された急峻なダウンヒルトラックが舞台になる。トラック上ではプッシングやスライディング、全力疾走など、ありとあらゆるアクションが展開されるが、ルールは至ってシンプルで、最初にゴールに到達したライダーが勝利を手にする。</p>
    <p>雪山のスロープや市街地などに設置される極めて先進的なトラックで展開されるRed Bull Crashed Iceは、これまで延べ数十万人もの観客を集めてきた。初期はワンオフイベントとして開催されていたが、2010年以降は正式なアイスクロス・ダウンヒル・ワールドチャンピオンシップとして開催されている。</p>
