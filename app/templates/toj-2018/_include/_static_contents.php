<section class="about">
  
  <h2 class="about__heading"><img src="/assets/images/toj-2018/ttl_h2-about.png" alt="ABOUT Tour of Japanとは"></h2>
  
  <div class="about__text">
    <p>ツアー・オブ・ジャパンは、1982年から1995年まで14回に渡り開催されていた「国際サイクルロードレース」を継承する国内最大規模の自転車ロードレースです。<br>
        1996年、UCI（国際自転車競技連合）にステージレースとして公認されたことを機に、その名称をツアー・オブ・ジャパンに変更し、第1回大会が開催されました。<br>
        2013年の第16回大会より、レースクラスが国内のステージレースとしては最高クラスのUCI-2.1となり、世界に18チームしかない最高峰のUCIプロチームや、世界トップレベルで活躍する強豪選手が参戦、アジアでも最高レベルの国際レースとなります。<br>
        また、日本国内で行われるロードレースとしては、都府県をまたぐ唯一のステージレースであり、その規模は一般公道を使用する全てのスポーツイベントの中でも最大クラスといえます。<br>
        全ステージを通じて約35万人の観客が会場や沿道に集まり、日本国内では貴重な存在といえる公道開催型の自転車ロードレースとしてたくさんの声援を受けています。<br>
        更に、レースだけでなく各開催地では出場選手や関係者などが講師として参加する子供向け自転車教室や各種イベントなどを開き、ロードレース・自転車を通じて各開催地域との交流も積極的に進めています。</p>
